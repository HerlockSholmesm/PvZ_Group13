public class Cell {
    private boolean dryness;
    private boolean aqua;

    public boolean isDryness() {
        return dryness;
    }

    public void setDryness(boolean dryness) {
        this.dryness = dryness;
    }

    public boolean isAqua() {
        return aqua;
    }

    public void setAqua(boolean aqua) {
        this.aqua = aqua;
    }
}
